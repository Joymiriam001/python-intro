def addtwonumber():
    num=int(input("Enter a number: "))
    num2=int(input("Enter second number: "))
    print(f"The sum of {num} and {num2} is {num+num2}")


def multiplytwonumber():
    num=int(input("Enter a number: "))
    num2=int(input("Enter second number: "))
    print(f"The product of {num} and {num2} is {num*num2}")

 