# List datastructure its mutable meaning its changeable

cars=["Toyota", "Nissan","Mercedes"]
print(cars)
print(f"I Love {cars[1]}")

# tuple it uses normal brackets, ordered, immutable, has index (0,1,2)
fruits=("apples", "bananas", "pineapples")
print(fruits)

print(f"I love eating {fruits[2]}")

#set it is unordered, doesn't have index
comp={"dell", "hp", "toshiba"}
print(comp)

# dictionaries
employee={"name":"John","age":35,"salary":4000}

print(employee)
print(f"The name of the employee is {employee['name']}")